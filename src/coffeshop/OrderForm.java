    private void add_cartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_cartActionPerformed

        String customerName;
        int coffeIndex;
        int qty;

        customerName = customer_name.getText();
        coffeIndex = coffee_name.getSelectedIndex();
        qty = (int) quantity.getValue();

        String coffeName = coffeeList.get(coffeIndex).getCoffeeName();
        double coffePrice = coffeeList.get(coffeIndex).getCoffeePrice();

        double totalPrice = coffePrice * qty;

        DefaultTableModel model = (DefaultTableModel) billing_table.getModel();

        Object[] obj = new Object[4];
        obj[0] = customerName;
        obj[1] = coffeName;
        obj[2] = qty;
        obj[3] = totalPrice;
        model.addRow(obj);
        double totalGrand = Double.parseDouble(total_price.getText()) + totalPrice;
        total_price.setText(String.valueOf(totalGrand));

    }//GEN-LAST:event_add_cartActionPerformed

    private void check_outActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_outActionPerformed

        DefaultTableModel model = (DefaultTableModel) billing_table.getModel();
        loadOrdersFile();

        String cusName = "";
        String coffeeName = "";
        int qty = 0;
        double price = 0.0;

        for (int i = 0; i < model.getRowCount(); i++) {
            cusName = (String) model.getValueAt(i, 0);
            coffeeName = (String) model.getValueAt(i, 1);
            qty = (int) model.getValueAt(i, 2);
            price = (double) model.getValueAt(i, 3);
            ordersList.add(new Orders(cusName, coffeeName, qty, price));
        }

        try {
            
            outputStream = new ObjectOutputStream(new FileOutputStream(ordersFile));
            outputStream.writeObject(ordersList);
            outputStream.close();
            JOptionPane.showMessageDialog(null, "Successfully Orderd");

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_check_outActionPerformed

