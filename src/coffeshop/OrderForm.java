    public void loadFile() {
        if (coffeeFile.isFile()) {
            try {
                inputStream = new ObjectInputStream(new FileInputStream(coffeeFile));
                coffeeList = (ArrayList<Coffee>) inputStream.readObject();
                inputStream.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            System.out.println("File does't exists!");
        }
    }

    public void loadOrdersFile() {
        if (ordersFile.isFile()) {
            try {
                inputStream = new ObjectInputStream(new FileInputStream(ordersFile));
                ordersList = (ArrayList<Orders>) inputStream.readObject();
                inputStream.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            System.out.println("File does't exists!");
        }
    }

    private void fillCoffeeList() {

        for (int i = 0; i < coffeeList.size(); i++) {
            String coffee = coffeeList.get(i).getCoffeeName();
            coffee_name.addItem(coffee);
        }
    }

